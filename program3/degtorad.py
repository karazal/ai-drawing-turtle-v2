import math
import random

degrees = math.sqrt(random.randint(10, 40))
radians = degrees * math.pi / 180

print("Os graus são:", degrees)
print("Os radianos são:", radians)

#Explicação:
## A variável "degrees" tem o valor de uma raíz quadrada de um número aleatório entre 10 e 40.
## A variável "radians" tem o valor de uma fórmula matemática, similar a o Teorema de Pitágoras

## Cálculos traduzidos abaixo

## "graus = Raiz(aleatorio(10, 40))"
## "radianos = graus x Pi dividido por 180"

